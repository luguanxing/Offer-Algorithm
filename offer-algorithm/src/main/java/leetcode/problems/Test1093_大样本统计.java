    static class Solution {
        public double[] sampleStats(int[] count) {
            long numSize = 0;
            double numSum = 0;
            Double min = null;
            Double max = null;
            Double mode = null;
            int maxCnt = 0;
            for (int i = 0; i <= 255; i++) {
                int cnt = count[i];
                numSize += cnt;
                numSum += i * 1.0 * cnt;
                if (cnt > 0 && min == null) {
                    min = 1.0 * i;
                }
                if (cnt > 0) {
                    max = 1.0 * i;
                }
                if (cnt > maxCnt) {
                    maxCnt = cnt;
                    mode = 1.0 * i;
                }
            }
            Double mean = numSum / numSize;
            Double median = null;
            if (numSize % 2 == 1) {
                long idx = (numSize + 1) / 2;
                long size = 0;
                for (int i = 0; i <= 255; i++) {
                    if (size < idx && idx <= size + count[i]) {
                        median = 1.0 * i;
                    }
                    size += count[i];
                }
            } else {
                double num1 = 0;
                long idx1 = numSize / 2;
                double num2 = 0;
                long idx2 = idx1 + 1;
                long size = 0;
                for (int i = 0; i <= 255; i++) {
                    if (size < idx1 && idx1 <= size + count[i]) {
                        num1 = 1.0 * i;
                    }
                    if (size < idx2 && idx2 <= size + count[i]) {
                        num2 = 1.0 * i;
                    }
                    size += count[i];
                }
                median = (num1 + num2) * 0.5;
            }
            return new double[]{min, max, mean, median, mode};
        }
    }

